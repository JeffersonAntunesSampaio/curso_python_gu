"""
Tipo Booleano

Algebra Booleana, criada por Gearge Boole

2 constantes = Verdadeiro ou False

True -> Verdadeiro
False -> Falso
"""

ativo = False

print(ativo)

"""
Operações básicas
"""

# Negação (not):
print(not ativo)

# Ou (or):
print(not ativo or ativo)

# E (and):
print(not ativo and ativo)
