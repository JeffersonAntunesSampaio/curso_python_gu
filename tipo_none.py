"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido
também como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado

OBS:
    - O tipo nome é SEMPRE especificado com a primeira letra maiúscula.
    - O tipo None em Python é sempre considerado falso


Quando utilizamos?
    - Podemos utilizar None quando queremos criar uma variável e inicializa-la
    com um tipo sem tipo, antes de recebermos um valor final

"""

numeros = None

print(numeros)
print(type(numeros))
