"""
Ranges
- Precisamos conhecer o lopp for para usar os ranges
- Precisamos conhecer o range para trabalhar melhor com loops for

Ranges são utilizamos para gerar sequências numéricas, não de forma aleatória, mas sim de maneira especificada

"""

# Exemplo Forma 1
for num in range(11):
    print(num)

# Exemplo Forma 2
for num in range(1, 11):
    print(num)

# Exemplo Forma 3
for num in range(1, 11, 2):
    print(num)

# Exemplo Forma 3
for num in range(10, 0, -1):
    print(num)
