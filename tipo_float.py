"""
Tipo float

Tipo real. decimal

Casas decimais

OBS: O separador de casas decimais na programação é ponto e não virgula
"""

valor = 1.44
print(valor)
print(type(valor))

# Podemos converter
res = int(valor)
print(res)
print(type(res))

# Podemos trabalhar com numero complexos
complx = 5j
print(type(complx))
